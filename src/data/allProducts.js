// Export products sorted by sales rank (bestsellers first)
export const getProductsSortedByBestsellers = () => {
  return [...allProducts].sort((a, b) => {
    // First sort by bestseller status
    if (a.bestseller && !b.bestseller) return -1;
    if (!a.bestseller && b.bestseller) return 1;
    
    // Then by sales rank
    return a.salesRank - b.salesRank;
  });
};

export const getProductsByCategory = (category) => {
  if (category === 'all') return getProductsSortedByBestsellers();
  return getProductsSortedByBestsellers().filter(product => 
    product.category && product.category.includes(category)
  );
};